package listaContigua;

public class Item {
    private int codigo;
    private String nome;
    
    public Item(){
        this.codigo = 0;
        this.nome = null;
    }

    public int getCodigo() {
        return codigo;
    }

    public void setCodigo(int codigo) {
        this.codigo = codigo;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    @Override
    public String toString() {
        return "Item{" + "codigo= " + codigo + ", nome= " + nome + '}';
    }
}
